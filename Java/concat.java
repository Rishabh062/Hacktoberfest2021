public class concat {
    public static void main (String args[])
    {
        String s = "Hello";
        String str = "World";
        String a = s.concat(str);
        String h = s + str;
        System.out.println(s);
        System.out.println(str);
        System.out.println(a);
        System.out.println(h);
    }
}
